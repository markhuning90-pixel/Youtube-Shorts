def show_generation(generation):
    print("Thema:")
    print(generation.topic)

    print("\nTitel:")
    print(generation.title)

    print("\nBeschreibung:")
    print(generation.description)

    print("\nHashtags:")
    print(generation.hashtags)

    print("\nScript:")
    print(generation.script)

    print("\nStatus:")
    print(generation.status)
